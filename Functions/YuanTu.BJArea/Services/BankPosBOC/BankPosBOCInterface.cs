    public class BankPosStr
    {
        public BankPosStr()
        { }
        public BankPosStr(string strOut)
        { //004563510000000000123C000000001234010400567800005600000120151010162812120000000068
            ResultCode = strOut.Substring(0, 2);
            if (ResultCode != "00")
            {//交易失败
                return;
            }
            BankNo = strOut.Substring(2, 19).TrimEnd(' ');
            TransFlag = strOut.Substring(21, 1);
            Amt = int.Parse(strOut.Substring(22, 12).TrimStart('0'));
            CardIssuingCode = strOut.Substring(34, 4);
            TransSeq = strOut.Substring(38, 6);
            POSSeq = strOut.Substring(44, 6);
            ClearDate = strOut.Substring(50, 6);
            TransDateTime = strOut.Substring(56, 14);
            CenterSeq = strOut.Substring(70, 12);
            ComNo = strOut.Substring(82, 15);
            TerminalNo = strOut.Substring(97, 8);
        }
        public static implicit operator bool(BankPosStr result)
        {
            return result.ResultCode=="00";
        }
        public string ComNo;
        public string TerminalNo;
        public string UserId;
        public string TransFlag;
        public decimal Amt = 0;

        public string ResultCode;
        public string ResultMsg
        {
            get
            {
                if (DirErrorMsg.ContainsKey(ResultCode))
