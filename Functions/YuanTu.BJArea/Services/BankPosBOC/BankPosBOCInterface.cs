                else return "未知银行" + CardIssuingCode;
            }
        }
        public string TransSeq;//交易凭证号
        public string POSSeq;//授权号
        public string ClearDate;//批次号
        public string TransDateTime;//日期时间
        public string CenterSeq;//参考号

        public string GetBankPosStr()
        {
            string str = "";
            str += TerminalNo.PadRight(10, ' ');
            str += UserId.PadRight(10, ' ');
            str += TransFlag;
            str += Amt.ToString().PadLeft(12, '0');
            //str += new string(' ', 33);
            if (TransFlag == "H")
            {
                str += TransSeq;
            }
            else if (TransFlag == "R")//退货，接口未提供交易类型标识，不实现
            {
                str += TransSeq;
                str += POSSeq;
                str += ClearDate;
                str += TransDateTime;
                str += CenterSeq;
            }
            return str;
        }
